    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
    try {                                          
            Conexion ObjetoConexion = new Conexion();
            
            String folio = txtfolio.getText();
            if (folio.trim().equals("")) {
                JOptionPane.showMessageDialog(this, "Complete el espacio en blanco"); 
            }
            
            String query = "Select * from boleta where folio = '"+folio+"'";
            
            state = ObjetoConexion.getCon().createStatement();
            
            res = state.executeQuery(query);
            
            if (res.next()) {
                lblprecioneto.setText("Precio Neto :" + res.getString("Precio Neto"));
                lblprecioconiva.setText("Precio con IVA :" + res.getString("Precio con iva"));
                lblcostoiva.setText("Costo IVA :" + res.getString("Costo IVA"));
                lblfechacompra.setText("Fecha de Compra :" + res.getString("Fecha de Compra"));
                lblhoradecompra.setText("Hora de Compra :" + res.getString("Hora de Compra"));
                lbldistribuidora.setText("Distribuidor involucrado :" + res.getString("Distribuidor involucrado"));
                lblmetododepago.setText("Metodo de Pago :" + res.getString("Metodo de Pago"));
                
                
            }
            else{
                JOptionPane.showMessageDialog(this, "No hay Registro");
                
                txtfolio.setText("");
                lblprecioneto.setText("Precio Neto :");
                lblprecioconiva.setText("Precio con IVA :");
                lblcostoiva.setText("Costo IVA :");
                lblfechacompra.setText("Fecha de Compra :");
                lblhoradecompra.setText("Hora de Compra :");
                lbldistribuidora.setText("Distribuidor involucrado :");
                lblmetododepago.setText("Metodo de Pago :");

            }
        }
        catch (SQLException ex) {
            Logger.getLogger(BuscarAutor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnbuscarActionPerformed

