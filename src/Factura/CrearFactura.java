    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    try {                                             
            
            Conexion ObjetoConexion = new Conexion();

            String folio_fac = txtfolio.getText();
            String precio_neto = txtprecioneto.getText();
            String costo_iva = txtcostoiva.getText();
            String precio_total = txtpreciototal.getText();
            String fecha_compra = txtfechadecompra.getText();
            String hora = txthoradecomprar.getText();
            String cod_metod = txtmetododepago.getText();
            String rut_dis = txtdistribuidor.getText();
            
            int folio  = 0;
                 
            if (folio_fac.trim().equals("")|| precio_neto.trim().equals("")|| costo_iva.trim().equals("")||
                    precio_total.trim().equals("")|| fecha_compra.trim().equals("")|| hora.trim().equals("")||
                    cod_metod.trim().equals("")||rut_dis.trim().equals("")) {
                
                JOptionPane.showMessageDialog(this, "Complete los espacios en blanco");
            }
        try{
            
            folio =Integer.parseInt(folio_fac);

            String query = "insert into factura(folio_fac,precio_neto,costo_iva,precio_total,fecha_compra,hora,cod_metod,rut_dis) "
                    + "values ('"+folio_fac+"','"+precio_neto+"','"+costo_iva+"'"
                    + ",'"+precio_total+"','"+fecha_compra+"','"+hora+"','"+cod_metod+"','"+rut_dis+"')";
            
           
            
            state = ObjetoConexion.getCon().createStatement();

            try{
                int x = state.executeUpdate(query);

                if (x > 0) {
                JOptionPane.showMessageDialog(this, "Registrado Correctamente");
                }
                else{
                JOptionPane.showMessageDialog(this, "No se pudo Registrar");
                }
            }
            catch(SQLException error){
                JOptionPane.showMessageDialog(this, "Folio ya registrado");
                System.out.println(state.executeUpdate(query));
               
            }

            }
        catch(NumberFormatException error){
        }


        } catch (SQLException ex) {
        Logger.getLogger(CrearFactura.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

