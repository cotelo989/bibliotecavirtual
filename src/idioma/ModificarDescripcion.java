    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        try {
            Conexion ObjetoConexion = new Conexion();

            String codigo_idioma = txtcodigo.getText();

            if (codigo_idioma.trim().equals("")) {
                JOptionPane.showMessageDialog(this, "Complete los espacios en blanco");
            }

            String query = "select * from idioma where codigo_estado ='"+codigo_idioma+"'";

            state = ObjetoConexion.getCon().createStatement();

            res = state.executeQuery(query);

            if (res.next()) {
                txtlengua.setText(res.getString("lengua"));
            }
            else{
                JOptionPane.showMessageDialog(this, "No hay registros");
                txtcodigo.setText("");
                txtlengua.setText("");
            }
        }
        catch (SQLException ex) {
            Logger.getLogger(ModificarDescripcion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnbuscarActionPerformed

    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed
        try {
            Conexion ObjetoConexion = new Conexion();

            String codigo_idioma = txtcodigo.getText();
            String lengua = txtlengua.getText();

            String query ="update idioma set lengua ='"+lengua+"'where codigo_idioma = '"+codigo_idioma+"'";;

            state = ObjetoConexion.getCon().createStatement();
            int x = state.executeUpdate(query);

            if (x > 0) {
                JOptionPane.showMessageDialog(this, "Actualizado");
            }
            else{
                JOptionPane.showMessageDialog(this, "No se pudo Actualizar");
            }
        }
        catch (SQLException ex) {
            Logger.getLogger(ModificarDescripcion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnmodificarActionPerformed

